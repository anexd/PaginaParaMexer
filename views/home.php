            <h2 class="flow-text black">Qual o objetivo?</h2>
            <p class="flow-text">O processamento de imagens tem como funções facilitar a visualização da imagem ou
                adequá-la para análises quantitativas através de correções de defeitos ou realces das regiões de
                interesse nas imagens; e a extração e tratamento de dados
                quantitativos, feitos pelo próprio computador. (Gomes, 2001). </p>
